package Encapsulation;

public class Encapsulation {
	
	private String name = "Mortoza";
	private int age = 30;
	
	
	public void setName (String newName) {
		this.name = newName;
	}
	
	public String getName() {
	   return name;	
	}
	
	public void setAge(int newAge) {
		this.age = newAge;	}
	
	public int getAge()
	{
		return age;
	}
	

}
