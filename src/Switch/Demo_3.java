package Switch;

public class Demo_3 {

	public static void main(String[] args) {
	
	int score = 10;	
	
	switch (score)
	
	{
	 
	case 10: System.out.println("Grade F");  break;
	case 20: System.out.println("Grade F");  break;
	case 30: System.out.println("Grade F");  break;
	case 40: System.out.println("Grade: D"); break;
	case 50: System.out.println("Grade: C"); break;
	case 60: System.out.println("Grade: B"); break;
	case 70: System.out.println("Grade: A"); break;
	case 80: System.out.println("Grade A+"); break;
	default: System.out.println("Invalid Number");
	
	
	}

	}

}
