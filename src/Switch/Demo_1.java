package Switch;

public class Demo_1 {

public static void main(String[] args) {
	
	
	int score = 80;
	
	switch (score)
	
	{
	
	case 50 : System.out.println("D Grade");break;
	case 60:  System.out.println("C Grade");break;
	case 70:  System.out.println("B Grade");break;
	case 80:  System.out.println("A Grade");break;
	default:  System.out.println("A+ Grade");
	
	
	}

	}

}
