package ifElse;

public class ifElse_Demo6 {

public static void main(String[] args) {
	
	int X = 10000;	
	int Y = 2000;
	int Z = 3000;
	
	if (X>Y && X>Z)
		
	{
		System.out.println("X is the Greatest");
	}

	else if (Y>X && Y>Z)
		
	{
	    System.out.println("Y is the Greatest");	
	}
	
	else
		
	{
		System.out.println("Z is the Greatest");
	}
	
	}

}
